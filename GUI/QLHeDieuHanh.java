    @Override
    public void edit() {
        if (tabs.getSelectedIndex() == 0) {

            String idhdh = (String) tblDSD.getValueAt(this.row, 0);

            BusHeDieuHanhModel hdhmodel = this.hdhser.selectByID(idhdh);
            setForm(hdhmodel);
            updateStatus();
        } else {
            String idhdh = (String) tblNSD.getValueAt(this.row, 0);

            BusHeDieuHanhModel hdhmodel = this.hdhser.selectByID(idhdh);
            setForm(hdhmodel);
            updateStatus2();

        }
    }

