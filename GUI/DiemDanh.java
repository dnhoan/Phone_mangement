    public void DiMuon() {
        String manv = (String) tblDiemDanh.getValueAt(this.row, 0);
        BusDiemDanh modell = new BusDiemDanh();
        modell.setManv(manv);
        try {
            this.diemdanh.DiMuon(modell);
            JOptionPane.showMessageDialog(this, "điểm danh thành công!");
        } catch (Exception e) {
        }
    }

