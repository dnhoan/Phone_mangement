    RomService romService = new RomService();

    @Override
    public void init() {
        this.setLocationRelativeTo(null);
        this.setTitle("");
        this.fillTable();
        this.fillTableXoa();
    }

    @Override
    public BusRomModel getForm() {
        BusRomModel busRomModel = new BusRomModel();
        busRomModel.setLoaiRon(txtLoaiRam.getText());
        busRomModel.setDungLuongRom(Float.parseFloat(txtDungLuong.getText()));
        return busRomModel;
    }

    @Override
    public void setForm(BusRomModel busRomModel) {
        txtLoaiRam.setText(busRomModel.getLoaiRon());
        txtDungLuong.setText(busRomModel.getDungLuongRom() + "");
    }

    @Override
    public void updateStatus() {
        boolean edit = (this.row >= 0);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
    }

    @Override
    public void insert() {
        BusRomModel busRomModel = this.getForm();
        try {
            romService.insert(busRomModel);
            this.fillTable();
            this.clearForm();
            this.updateStatus();
            MessageService.alert(this, "ok");
        } catch (Exception e) {
            MessageService.alert(this, "err");
        }
    }

    @Override
    public void update() {
        BusRomModel busRomModel = this.getForm();
        int id = (int) tblRom.getValueAt(this.row, 0);
        busRomModel.setMaRom(id);
        try {
            romService.update(busRomModel);
            this.fillTable();
            MessageService.alert(this, "ok");
        } catch (Exception e) {
            MessageService.alert(this, "err");
        }
    }

    @Override
    public void delete() {
        int id = (int) tblRom.getValueAt(this.row, 0);
        try {
            romService.delete(id);
            this.fillTable();
            this.fillTableXoa();
            this.clearForm();
            MessageService.alert(this, "ok");
        } catch (Exception e) {
            MessageService.alert(this, "err");
        }
    }

    @Override
    public void edit() {
        int id = (int) tblRom.getValueAt(this.row, 0);
        BusRomModel busRomModel = this.romService.selectByID(id);
        this.setForm(busRomModel);
        this.updateStatus();
    }

    @Override
    public void clearForm() {
        BusRomModel busRomModel = new BusRomModel();
        this.row = -1;
        this.setForm(busRomModel);
        this.updateStatus();
    }

    @Override
    public void fillTable() {
        this.row = -1;
        this.romModel = (DefaultTableModel) tblRom.getModel();
        this.romModel.setRowCount(0);
        try {
            this.listRom = romService.selectAll();
            this.listRom.forEach((r) -> {
                romModel.addRow(new Object[]{
                    r.getMaRom(),
                    r.getLoaiRon(),
                    r.getDungLuongRom()
                });
            });
            this.updateStatus();
        } catch (Exception e) {
            System.out.println("loi rom");
            e.printStackTrace();
        }
    }
    public void fillTableXoa() {
        this.rowRecycle = -1;
        this.romRecycleModel = (DefaultTableModel) tblXoa.getModel();
        this.romRecycleModel.setRowCount(0);
        try {
            this.listRomRecycle = romService.selectRecycle();
            if(this.listRomRecycle != null) {
                this.listRomRecycle.forEach((r) -> {
                    romRecycleModel.addRow(new Object[]{
                        r.getMaRom(),
                        r.getLoaiRon(),
                        r.getDungLuongRom(),
                    });
                });
            }
            this.updateRecycle();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void backup() {
        int id = (int) tblXoa.getValueAt(this.rowRecycle, 0);
        try {
            romService.backup(id);
            this.fillTable();
            this.fillTableXoa();
            this.updateRecycle();
            MessageService.alert(this, "ok");
        } catch (Exception e) {
            MessageService.alert(this, "err");
        }
    }
    public void updateRecycle() {
        boolean edit = (this.rowRecycle >= 0);
        btnbackup.setEnabled(edit);
    }

