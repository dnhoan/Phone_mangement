    private void innit() {
        this.setLocationRelativeTo(null);
        new Timer(60, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
               int  value = pgbloading.getValue();
               if(value < pgbloading.getMaximum()){
                   pgbloading.setValue(value+1);
               }else{
                   load.this.dispose();
               }
            }
        }).start();
        
    }

