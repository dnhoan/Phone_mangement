    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String ghiChu = txtLyDo.getText();
        String commit = AuthService.user.getMaNV() + " " + DateService.toString(new Date(), "ss:mm:hh dd/MM/yyyy");
        hoaDonService.updateStatus(this.status, this.mahd, ghiChu + "\n " + commit);
        QuanLyHoaDon.getDataHoaDon();
        if(status == 0) {
            MessageService.alert(rootPane, "Xóa hóa đơn thành công !");
        } else {
            MessageService.alert(rootPane, "Khôi phục hóa đơn thành công !");
        }
        this.dispose();
    }//GEN-LAST:event_btnThemActionPerformed

    private void txtLyDoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtLyDoKeyReleased
        String lyDo = txtLyDo.getText();
        if(lyDo.isBlank() || lyDo.isEmpty()) {
            btnThem.setEnabled(false);
            lblCount.hide();
        }  else {
            lblCount.show();
            lblCount.setText("Số ký tự: "+lyDo.length() + "/30");
            if(lyDo.length() > 30) {
                btnThem.setEnabled(true);
            } else {
                btnThem.setEnabled(false);
            }
        } 
    }//GEN-LAST:event_txtLyDoKeyReleased

